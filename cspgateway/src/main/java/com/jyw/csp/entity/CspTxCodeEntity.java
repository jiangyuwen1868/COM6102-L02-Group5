package com.jyw.csp.entity;

public class CspTxCodeEntity {

	private String txcode;
	private String txname;
	private String txtype;
	private String isoutbound;
	private String ptxcode;
	private String groupid;
	private String remark;
	public String getTxcode() {
		return txcode;
	}
	public void setTxcode(String txcode) {
		this.txcode = txcode;
	}
	public String getTxname() {
		return txname;
	}
	public void setTxname(String txname) {
		this.txname = txname;
	}
	public String getTxtype() {
		return txtype;
	}
	public void setTxtype(String txtype) {
		this.txtype = txtype;
	}
	public String getIsoutbound() {
		return isoutbound;
	}
	public void setIsoutbound(String isoutbound) {
		this.isoutbound = isoutbound;
	}
	public String getPtxcode() {
		return ptxcode;
	}
	public void setPtxcode(String ptxcode) {
		this.ptxcode = ptxcode;
	}
	public String getGroupid() {
		return groupid;
	}
	public void setGroupid(String groupid) {
		this.groupid = groupid;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
